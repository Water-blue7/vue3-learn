<template>
    <div class="person">
        <h2>姓名：{{ name }}</h2>
        <h2>年龄：{{ age }}</h2>
        <button @click="changeName">修改名字</button>
        <button @click="changeAge">修改年龄</button>
        <!-- <h2>电话：{{ tel }}</h2> -->
        <button @click="showTel">查看联系方式</button>
    </div>
</template>

<script lang="ts">
export default {
    name: "Person", //组件名
    data() {
        return {
            name: "张三",
            age: 19,
            tel: "133333333",
        };
    },
    methods: {
        changeName() {
            this.name = "zhangsan";
        },
        changeAge() {
            this.age += 1;
        },
        showTel() {
            alert(this.tel);
        },
    },
};
</script>


